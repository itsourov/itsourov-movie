        <script type="text/javascript">
            var accessToken = '';
            var refreshed = false;
            $(document).ready(function() {



                regenerateToken('{{ route('bkash.token') }}');
                var paymentConfig = {
                    createCheckoutURL: "{{ route('bkash.payment.create') }}",
                    executeCheckoutURL: "{{ route('bkash.payment.execute') }}",
                };

                var paymentRequest;
                paymentRequest = {
                    amount: '105',
                    intent: 'sale'
                };

                bKash.init({
                    paymentMode: 'checkout',
                    paymentRequest: paymentRequest,
                    createRequest: function(request) {


                        $.ajax({
                            headers: {
                                'X-CSRF-TOKEN': '{{ csrf_token() }}'
                            },
                            url: paymentConfig.createCheckoutURL + "?amount=" + paymentRequest
                                .amount,
                            type: 'GET',
                            contentType: 'application/json',
                            success: function(data) {



                                var obj = JSON.parse(data);

                                if (data) {

                                    if (!refreshed) {
                                        if (obj.message == 'Unauthorized' || obj.message ==
                                            'The incoming token has expired') {
                                            regenerateToken(
                                                '{{ route('bkash.token.refresh') }}',
                                                true);
                                        }
                                    }
                                    if (data && obj.paymentID != null) {
                                        paymentID = obj.paymentID;
                                        bKash.create().onSuccess(obj);
                                    } else {

                                        $('#bkash-error-modal-title').text(
                                            'Unexpected Error!! ' + obj.errorMessage)

                                        window.dispatchEvent(new CustomEvent('open-modal', {
                                            detail: 'bkash-error-modal'
                                        }));
                                        bKash.create().onError();
                                    }

                                } else {
                                    $('#bkash-error-modal-title').text('Unexpected Error!')

                                    window.dispatchEvent(new CustomEvent('open-modal', {
                                        detail: 'bkash-error-modal'
                                    }));
                                }

                                $('#loading-overlay').hide();
                            },
                            error: function(e) {
                                $('#loading-overlay').hide();
                                bKash.create().onError();

                                $('#bkash-error-modal-title').text(
                                    'Unexpected Error! ' + e
                                    .statusText)

                                window.dispatchEvent(new CustomEvent('open-modal', {
                                    detail: 'bkash-error-modal'
                                }));

                            }
                        });
                    },

                    executeRequestOnAuthorization: function() {
                        console.log('=> executeRequestOnAuthorization');
                        $.ajax({
                            url: paymentConfig.executeCheckoutURL + "?paymentID=" +
                                paymentID,
                            type: 'GET',
                            contentType: 'application/json',
                            success: function(data) {

                                data = JSON.parse(data);
                                if (data && data.paymentID != null) {
                                    alert('[SUCCESS] data : ' + JSON.stringify(data));

                                    console.log(data);
                                } else {
                                    bKash.execute().onError();

                                    $('#bkash-error-modal-title').text(data
                                        .errorMessage)

                                    window.dispatchEvent(new CustomEvent('open-modal', {
                                        detail: 'bkash-error-modal'
                                    }));
                                }
                            },
                            error: function(e) {
                                bKash.execute().onError();


                                $('#bkash-error-modal-title').text(
                                    'Unexpected Error! ' + e
                                    .statusText)

                                window.dispatchEvent(new CustomEvent('open-modal', {
                                    detail: 'bkash-error-modal'
                                }));

                            }
                        });

                    },
                    onClose: function() {
                        $('#bkash-error-modal-title').text('Payment window closed')

                        window.dispatchEvent(new CustomEvent('open-modal', {
                            detail: 'bkash-error-modal'
                        }));
                    },

                });




            });
            $("#bKash_button").click(function() {
                $('#loading-overlay').show();
            });

            function callReconfigure(val) {
                bKash.reconfigure(val);
            }

            function clickPayButton() {
                $("#bKash_button").trigger('click');

            }

            function postRefresh() {
                $("#bKash_button").trigger('click');
                refreshed = true;
            }

            function regenerateToken(tokenUrl, refreshing = false) {
                $.ajax({
                    headers: {
                        'X-CSRF-TOKEN': '{{ csrf_token() }}'
                    },
                    url: tokenUrl,
                    type: 'POST',
                    contentType: 'application/json',
                    success: function(data) {

                        accessToken = JSON.stringify(data);

                        if (refreshing) {
                            postRefresh();
                        }
                    },
                    error: function(e) {

                        $('#bkash-error-modal-title').text('Unexpected Error! ' + e.statusText)

                        window.dispatchEvent(new CustomEvent('open-modal', {
                            detail: 'bkash-error-modal'
                        }));

                    }
                });
            }
